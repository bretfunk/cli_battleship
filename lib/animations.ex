      :timer.sleep(250)
      IO.puts("|____/_/    \\_\\_|     |_|  |______|______|_____/|_|  |_|_____|_|")
      :timer.sleep(250)
    else
      nil
    end
  end

  def pause_animation(num) do
    if DevConfig.animations() do
      if num === 0 do
        nil
      else
        IO.puts(" ")
        :timer.sleep(250)
        pause_animation(num - 1)
      end
    else
      nil
    end
  end

  def start_animation(num) do
    if DevConfig.animations() do
      Animations.clear_screen()

      case num do
        3 ->
          IO.puts("""
                              /|
                            ///|
                          /////|
                        ///////|
                      /////////|
                \==========|===/
          ~~~~~~~~~~~~~~~~~~~~~
          """)

          :timer.sleep(1000)
          start_animation(num - 1)

        2 ->
          IO.puts("""
                                                  /|
                                                ///|
                                              /////|
                                            ///////|
                                          /////////|
                                    \==========|===/
          ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
          """)

          :timer.sleep(1000)
          start_animation(num - 1)

        1 ->
          IO.puts("""
                                                                      /|
                                                                    ///|
                                                                  /////|
                                                                ///////|
                                                              /////////|
                                                        \==========|===/
          ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
          """)

          :timer.sleep(1000)
          start_animation(num - 1)

        0 ->
          enemy_cutscene("""
          SURRENDER PIRATES,
          WE HAVE YOU SURROUNDED
          """)

          Music.lose_music()

        _ ->
          nil
      end
    else
      nil
    end
  end

  def miss_animation() do
