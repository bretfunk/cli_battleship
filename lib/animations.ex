defmodule Animations do
  def clear_screen do
    IO.puts("\e[H\e[2J")
  end

  def intro_animation() do
    if DevConfig.animations() do
      :timer.sleep(250)
      IO.puts(" ____       _______ _______ _      ______  _____ _    _ _____ _____  ")
      :timer.sleep(250)
